"""
Tipo None

O tipo dado None em Python representa o tipo sem tipo, ou poderia ser conhecido também como tipo vazio

OBS: sempre representado com o 'n' maiusculo

Certo: Nome
Errado: none

Igual ao null do C#

OBS: O tipo none é sempre considerado false
"""

numeros = None

print(numeros)
print(type(numeros))

numeros = 1.44, 1.23, 1.90

print(numeros)
print(type(numeros))
